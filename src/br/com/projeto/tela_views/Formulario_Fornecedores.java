    private void btBusca2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBusca2ActionPerformed
            // botão buscar cliente por nome
            
               String nome = jTextNome.getText();
               Fornecedores obj = new Fornecedores();
               FornecedoresDAO dao = new FornecedoresDAO();
               
               obj = dao.ConsultaPorNome(nome);
               
               if(obj.getNome() != null ){
               
               
               //Exibe os dados do obj nos campos de texto
               jTextcod.setText(String.valueOf(obj.getId()));
               jTextNome.setText(obj.getNome());
               jFCNPJ.setText(obj.getCnpj());
               
               jTextEmail.setText(obj.getEmail());
               jTel.setText(obj.getTelefone());
               jCel.setText(obj.getCelular());
               
               jFcep.setText(obj.getCep());
               jendereco.setText(obj.getEndereço());
               jTnum.setText(String.valueOf(obj.getNumero()));
               jTcomplem.setText(obj.getComplemento());
               jTbairro.setText(obj.getBairro());
               jTcidade.setText(obj.getCidade());
               cbuf.setSelectedItem(obj.getUf());
               }
               else{
                   JOptionPane.showMessageDialog(null, "Cliente não encontrado");
               }
                   
    }//GEN-LAST:event_btBusca2ActionPerformed

