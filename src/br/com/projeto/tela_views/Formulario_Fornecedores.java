    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
            String nome = "%" +jTpesquisa2.getText()+ "%";  //inicio de qualquer carcter + minha pesquisa + qualquer carcater

        FornecedoresDAO dao = new FornecedoresDAO();
        List<Fornecedores> lista = dao.BuscarFornecedoresPorNome(nome);

        DefaultTableModel dados = (DefaultTableModel) TabelaFornecedores.getModel(); //criação do objeto que vai armazenar os dados dessa lista para exibição na tabela de clientes
        dados.setNumRows(0); //limpa os dados

        for (Fornecedores c : lista) { //para cada item da lista vai criar um obj chamado c 
            dados.addRow(new Object[]{
                //definição de ordem para aparecer na tabela
                c.getId(),
                c.getNome(),
                c.getCnpj(),
                c.getEmail(),
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereço(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getUf()
            }); //retorno de objetos

        }
    }//GEN-LAST:event_btPesquisarActionPerformed

