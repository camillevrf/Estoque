    private void btConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConsultarActionPerformed
        // bot√£o pesquisar venda por periodo
        try {
            //receber as datas e formatar para o banco de dados
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");

            LocalDate data_venda = LocalDate.parse(jFdataVenda.getText(),formato);
           
             double total_venda;
            
            
            VendasDAO dao = new VendasDAO();
            total_venda = dao.retornaTotalVendaPorData(data_venda);
                    
            jTextTTvenda.setText(String.valueOf(total_venda));
            
        } catch (Exception erro) {
            JOptionPane.showConfirmDialog(null, "Informe uma data corretamente");
        }
    }//GEN-LAST:event_btConsultarActionPerformed

