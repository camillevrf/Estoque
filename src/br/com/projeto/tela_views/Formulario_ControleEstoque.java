    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        //
        try{
         
          int qtd_estoque,qtd;  
          
          qtd_estoque = Integer.parseInt(jTextEstoque.getText()); 
          qtd = Integer.parseInt(jTextQuantidade.getText());
         
          qtd_nova= qtd_estoque + qtd;
          
          ProdutosDAO dao = new ProdutosDAO();
          dao.adicionarEstoque(idproduto, qtd_nova);
        
          JOptionPane.showMessageDialog(null, "Estoque do Produto Atualizado!");
        }catch(Exception e){
           JOptionPane.showMessageDialog(null, "Selecione o produto ou a nova quantidade." + e);  
        }
    }//GEN-LAST:event_btAdicionarActionPerformed

    private void TabelaEstoquedeProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaEstoquedeProdutosMouseClicked
        // Pegar os dados pessoais armazenados
        
        idproduto = Integer.parseInt(TabelaEstoquedeProdutos.getValueAt(TabelaEstoquedeProdutos.getSelectedRow(), 0).toString());
        jTextEstoque.setText(TabelaEstoquedeProdutos.getValueAt(TabelaEstoquedeProdutos.getSelectedRow(), 3).toString());
        jTextdescricao.setText(TabelaEstoquedeProdutos.getValueAt(TabelaEstoquedeProdutos.getSelectedRow(), 1).toString());

    }//GEN-LAST:event_TabelaEstoquedeProdutosMouseClicked

