    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // botão para salvar
        Produtos obj = new Produtos();

       obj.setDescricao(jTextdescricao.getText());
       obj.setPreco(Double.parseDouble(jTextPreco.getText()));
       obj.setQtd_estoque(Integer.parseInt(jTextEstoque.getText()));
       
       //Objeto de Fornecedor
       Fornecedores f = new Fornecedores();
       f = (Fornecedores)cbFornecedor.getSelectedItem();    //o objeto do selectItem é capturado e converte(castting) em um objeto fornecedor armazenando no f  
       obj.setFornecedor(f);

        ProdutosDAO dao = new ProdutosDAO();

        dao.cadastrarProdutos(obj);
        
        new Utilitarios().Limpatela(painelDados);


    }//GEN-LAST:event_btSalvarActionPerformed

