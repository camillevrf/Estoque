    private void jTpesquisa2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTpesquisa2KeyPressed
          String nome = "%" +jTpesquisa2.getText()+ "%";   
        
       ProdutosDAO dao = new ProdutosDAO();
       List<Produtos> lista = dao.BuscarProdutosPorNome(nome);

        DefaultTableModel dados = (DefaultTableModel) TabelaProdutos.getModel(); //criação do objeto que vai armazenar os dados dessa lista para exibição na tabela de clientes
        dados.setNumRows(0); //limpa os dados

        for (Produtos c : lista) { //para cada item da lista vai criar um obj chamado c 
            dados.addRow(new Object[]{
                //definição de ordem para aparecer na tabela
                c.getId(),
                c.getDescricao(),
                c.getPreco(),
                c.getQtd_estoque(),
                c.getFornecedor().getNome()
            });
        }      

    }//GEN-LAST:event_jTpesquisa2KeyPressed

