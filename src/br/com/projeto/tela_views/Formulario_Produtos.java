    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
           
            String nome = jTextdescricao.getText();
               Clientes obj = new Clientes();
               ClientesDAO dao = new ClientesDAO();
               
               obj = dao.ConsultaPorNome(nome);
               
               if(obj.getNome() != null ){
               
               
               //Exibe os dados do obj nos campos de texto
               jTextcod.setText(String.valueOf(obj.getId()));
               jTextdescricao.setText(obj.getNome());
              
               jTextPreco.setText(obj.getEmail());
              
              cbFornecedor.setSelectedItem(obj.getUf());
               }
               else{
                   JOptionPane.showMessageDialog(null, "Produto não encontrado");
               }

        
    }//GEN-LAST:event_btPesquisarActionPerformed

    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        // botão editar

        Produtos obj = new Produtos();

        obj.setId(Integer.parseInt(jTextcod.getText()));
        obj.setDescricao(jTextdescricao.getText());
        obj.setPreco(Double.parseDouble(jTextPreco.getText()));
        obj.setQtd_estoque(Integer.parseInt(jTextEstoque.getText()));
            
            //cria um obj de fornecedor 
        Fornecedores f = new Fornecedores();
        f = (Fornecedores)cbFornecedor.getSelectedItem(); //fornecedor selecionado no obj é armazenado
        
        obj.setFornecedor(f);   //passa o obj para o produto
       
        ProdutosDAO dao = new ProdutosDAO();
        dao.alterarProdutos(obj);
        
        new Utilitarios().Limpatela(painelDados);
        
    }//GEN-LAST:event_btEditarActionPerformed

    private void btBusca2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBusca2ActionPerformed
            // botão buscar cliente por nome
           String nome = jTextdescricao.getText();
                Produtos obj = new Produtos();
               ProdutosDAO dao = new ProdutosDAO();
               
               obj = dao.ConsultaPorNome(nome);
               
               cbFornecedor.removeAllItems();
               
               if(obj.getDescricao() != null ){
               
               
               //Exibe os dados do obj nos campos de texto
               jTextcod.setText(String.valueOf(obj.getId()));
               jTextdescricao.setText(obj.getDescricao());
               jTextPreco.setText(String.valueOf(obj.getPreco()));
               jTextEstoque.setText(String.valueOf(obj.getQtd_estoque()));
              
               
               Fornecedores f = new Fornecedores();
               FornecedoresDAO fdao = new FornecedoresDAO();
               
               f = fdao.ConsultaPorNome(obj.getFornecedor().getNome());
               
               cbFornecedor.getModel().setSelectedItem(f);
               }
               else{
                   JOptionPane.showMessageDialog(null, "Produto não encontrado");
               }  
                   
    }//GEN-LAST:event_btBusca2ActionPerformed

