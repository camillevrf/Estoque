    private void btBusca2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBusca2ActionPerformed
        // botão adicionar item
        qtd = Integer.parseInt(jTextQtdd.getText());
        preco = Double.parseDouble(jTextPreco.getText());
        
        subtotal = qtd*preco;
        
        total += subtotal;   //calculo
        
       jTttVenda.setText(String.valueOf(total));
       
       // Adicionar o produto na tabela
       carrinho = (DefaultTableModel)TabelaItens.getModel();
       
       carrinho.addRow(new Object[]{    //pega diretamente dos campos 
           jTextCod.getText(),
           jTextProduto.getText(),
           jTextQtdd.getText(),
           jTextPreco.getText(),
           subtotal
       });
       

    }//GEN-LAST:event_btBusca2ActionPerformed

