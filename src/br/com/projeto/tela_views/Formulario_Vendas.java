    private void jTextCodKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextCodKeyPressed
        // Busca produto por cod
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            Produtos obj = new Produtos();
            ProdutosDAO dao = new ProdutosDAO();

            obj = dao.buscarPorcodigo(Integer.parseInt(jTextCod.getText()));

            jTextProduto.setText(obj.getDescricao());
            jTextPreco.setText(String.valueOf(obj.getPreco()));
        }
    }//GEN-LAST:event_jTextCodKeyPressed

